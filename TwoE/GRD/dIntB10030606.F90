            ! Generating (f0|s0)^(4)
            CALL VRRf0s0(56,56,VRR(1,1,4),VRR(1,1,5))
            ! Generating (f0|s0)^(3)
            CALL VRRf0s0(56,56,VRR(1,1,3),VRR(1,1,4))
