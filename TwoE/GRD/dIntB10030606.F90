            CALL VRRg0s0(56,56,VRR(1,1,5),VRR(1,1,6))
            ! Generating (g0|s0)^(4)
            CALL VRRg0s0(56,56,VRR(1,1,4),VRR(1,1,5))
            ! Generating (g0|s0)^(3)
