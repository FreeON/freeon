      REAL(DOUBLE), DIMENSION(20,4,1) :: HRR 
      REAL(DOUBLE), DIMENSION(35,4,1) :: HRRA,HRRB 
      REAL(DOUBLE), DIMENSION(20,10,1) :: HRRC 
