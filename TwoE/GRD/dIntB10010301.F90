              SqInvT=SqInvT*InvT
              VRR(1,1,4)=+5.815864198283724D+00*Upq*SqInvT
              SqInvT=SqInvT*InvT
              VRR(1,1,5)=+2.617138889227676D+01*Upq*SqInvT
            ENDIF
            ! Generating (p0|s0)^(4)
            VRR(2,1,4)=PAx*VRR(1,1,4)+WPx*VRR(1,1,5) 
            VRR(3,1,4)=PAy*VRR(1,1,4)+WPy*VRR(1,1,5) 
            VRR(4,1,4)=PAz*VRR(1,1,4)+WPz*VRR(1,1,5) 
