            ! Generating (f0|s0)^(2)
            CALL VRRf0s0(35,10,VRR(1,1,2),VRR(1,1,3))
            ! Generating (f0|s0)^(1)
            CALL VRRf0s0(35,10,VRR(1,1,1),VRR(1,1,2))
