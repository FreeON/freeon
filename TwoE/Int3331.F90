      V(32)=QCx*V(11)
      V(33)=WQx*V(14)
      V(34)=QCx*V(15)
      V(35)=WQx*V(19)
      V(36)=QCx*V(21)
      V(37)=WQx*V(25)
      V(38)=HfxZpE*V(14)
      V(39)=AuxR2*HfxZpE
      V(40)=AuxR2*PAx
      V(41)=AuxR3*WPx
      V(42)=V(40)+V(41)
      V(43)=AuxR1*QCx
      V(44)=AuxR2*WQx
      V(45)=V(43)+V(44)
      V(46)=ExZpE*V(45)
      V(47)=-V(46)
      V(48)=V(29)+V(30)+V(47)
      V(49)=r1x2Z*V(48)
      V(50)=HfxZpE*V(19)
      V(51)=V(16)+V(20)
      V(52)=PAx*V(19)
      V(53)=AuxR2*PAy
      V(54)=AuxR3*WPy
      V(55)=V(53)+V(54)
      V(56)=WPx*V(55)
      V(57)=V(52)+V(56)
      V(58)=HfxZpE*V(25)
      V(59)=V(22)+V(26)
      V(60)=PAx*V(25)
      V(61)=AuxR2*PAz
      V(62)=AuxR3*WPz
      V(63)=V(61)+V(62)
      V(64)=WPx*V(63)
      V(65)=V(60)+V(64)
